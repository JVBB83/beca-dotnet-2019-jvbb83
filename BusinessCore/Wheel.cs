namespace BusinessCore
{
    class Wheel
    {
        private int ID;
        private int brand;
        private double size;

        public Wheel()
        {
            ID = 0;
            brand = 0;
            size = 0;
        }

        public void SetID(int i)
        {
            ID = i;
        }

        public int GetID()
        {
            return ID;
        }


    }







}
